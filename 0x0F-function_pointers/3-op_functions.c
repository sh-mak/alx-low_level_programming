#include "3-calc.h"
/**
 * op_add - adds two integers
 * @a: 1st integer
 * @b: 2nd integer
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op+sub - subtracts two integers
 * @a: 1st integer
 * @b: 2nd integer
 * Return: defference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies two integers
 * @a: 1st integer
 * @b: 2nd integer
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - devides two integers
 * @a: 1st integer
 * @b: 2nd integer
 * Return: quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo two integers
 * @a: 1st integer
 * @b: 2nd integer
 * Return: remainders
 */
int op_mod(int a, int b)
{
	return (a % b);
}
